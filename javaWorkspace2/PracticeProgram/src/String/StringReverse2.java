package String;

public class StringReverse2 {
     public static void main(String[] args) {
		String strName="Java Basic";
		String reverse=" ";
		System.out.println("Normal string print");
		System.out.println(strName);
		System.out.println("Reverse the String Print");
		for(int i=strName.length()-1;i>=0;i--)
		{
			reverse =reverse+strName.charAt(i);
		}
		System.out.println(reverse);
		//Palindrome the two string memebers
		if(strName.equalsIgnoreCase(reverse))
		{
			System.out.println("Palindrome of string is passed");
		}
		else 
		{
			System.out.println("Palindrome of string is failed");
		}
	}
}
