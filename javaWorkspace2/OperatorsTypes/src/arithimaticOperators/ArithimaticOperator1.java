package arithimaticOperators;

public class ArithimaticOperator1 {
   public static void main(String[] args) {
	 int number1=10,number2=20;
	 
	 //Addition Operation Use......
	 int sum=number1+number2;
	 System.out.println("Addition operation value: "+sum);

     // Subtraction Operation Use.....
	 int diff= number1-number2;
	 System.out.println("Subtraction Operation Use: "+diff);
	 
	 //Multiplication Operation Use.....
	 int mult=number1*number2;
	 System.out.println("Multiplication Operation Use: "+mult);
	 
	 //Division Operation Use.....
	 int div=number1/number2;
	 System.out.println("Division Operation Use: "+div);
	 
	 //Modulus Operation Use.....
	 int mul=number1%number2;
	 System.out.println("Modulus Operation Use: "+mul);
   }
}
