package comparisionOperationExample;

public class ComparisionOperation1 {
      public static void main(String[] args) {
		int num1=12,num2=4;
		
		//Greater than '>'
		System.out.println("num1>num2:"+(num1>num2));
		
	    //Less than '<'
		System.out.println("num1<num2: "+(num1<num2));
		
		//Is Equal to..'=='
		System.out.println("Is Equal to:"+(num1==num2));
		
		//Is Not Equal to..'!='
		System.out.println("Is Not Equal to:"+(num1!=num2));
		
		//Is Greater Than Equal to..'>='
	    System.out.println("Is grater than equal to:"+(num1>=num2));
	    
	    //Is Less than equal to...'<='
	    System.out.println("Is less than equal to:"+(num1<=num2));
	    
      }
}
